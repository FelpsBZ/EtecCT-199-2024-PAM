console.log('imprimindo de 1 a 5 com while');
var i = 1;
while(i <= 5) {
    console.log(i);
    i++;
}

console.log('imprimindo de 1 a 5 com do while');
var j = 1;
do{
    console.log(j);
    j++;
}while (j <= 5);

console.log('imprimindo de 1 a 5 com for');
for(var k = 1; k <= 5 ; k++) {
    console.log(k);
}